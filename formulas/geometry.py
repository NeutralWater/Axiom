import math

def circle_area(r):
    return math.pi * r ** 2

def rectangle_area(l, w):
    return l * w

def circle_circumference(r):
    return 2 * math.pi * r

def triangle_area(b, h):
    return 0.5 * b * h

def rectangle_perimeter(l, w):
    return 2 * (l + w)

def pythagorean(a, b):
    return math.sqrt(a ** 2 + b ** 2)